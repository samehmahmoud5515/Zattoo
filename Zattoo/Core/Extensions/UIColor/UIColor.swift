import UIKit

extension UIColor {
    @nonobjc class var groupLineGradientColor1: UIColor {
        return UIColor(named: "groupLineGradientColor1") ?? .white
    }
    
    @nonobjc class var groupLineGradientColor2: UIColor {
        return UIColor(named: "groupLineGradientColor2") ?? .white
    }
    
    @nonobjc class var black30: UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 3)
    }
}
